#Объединение
a = {0, 1, 2, 3}
b = {4, 3, 2, 1}
c = a.union(b)
print(c)
#{0, 1, 2, 3, 4}

#Добавление
a = {0, 1, 2, 3}
b = {4, 3, 2, 1}
a.update(b)
print(a)
#{0, 1, 2, 3, 4}

#Пересечение
a = {0, 1, 2, 3}
b = {4, 3, 2, 1}
c = a.intersection(b)
print(c)
#{1, 2, 3}

#Разноcть
a = {0, 1, 2, 3}
b = {4, 3, 2, 1}
c = a.difference(b)
print(c)
#{0}